//Fazer um programa para ler um número inteiro e dizer se este número é par ou ímpar.

public class division
{
    public static void main(String[] args)
    {
        int num;

        num = 12;

        if (num % 2 == 0)
        {
            System.out.println("PAR");
        }
        else
        {
            System.out.println("IMPAR");
        }
    }
}